package edu.vcentry.basics.session03.datatypes;

public class BooleanDataType {
	public static void main(String[] args) {
		boolean myBoolean1 = true;
		boolean myBoolean2 = false;

		System.out.println(myBoolean1);
		System.out.println(myBoolean2);

		// Boolean - Wrapper Class for primitive type boolean
	}

}

// In Java, valid boolean values are true and false (0 and 1 are not a valid boolean value)
// True or False (with T and F caps are not valid boolean values)
